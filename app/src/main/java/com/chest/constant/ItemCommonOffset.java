package com.chest.constant;

import android.content.Context;
import android.graphics.Rect;
import android.support.annotation.DimenRes;
import android.support.annotation.NonNull;
import android.support.v7.widget.RecyclerView;
import android.view.View;

/**
 * Created by TrungTV on 01/03/2018.
 */

public class ItemCommonOffset extends RecyclerView.ItemDecoration {
    private int mItemOffset;

    public ItemCommonOffset(int itemOffset) {
        mItemOffset = itemOffset;
    }

    public ItemCommonOffset(@NonNull Context context, @DimenRes int itemOffsetId) {
        this(context.getResources().getDimensionPixelSize(itemOffsetId));
    }

    @Override
    public void getItemOffsets(Rect outRect, View view, RecyclerView parent,
                               RecyclerView.State state) {
        super.getItemOffsets(outRect, view, parent, state);
        outRect.set(mItemOffset, mItemOffset, mItemOffset, mItemOffset);
    }
}
